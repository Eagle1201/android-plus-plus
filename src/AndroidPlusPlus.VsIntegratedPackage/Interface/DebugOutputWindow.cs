namespace AndroidPlusPlus.VsIntegratedPackage
{

  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public class DebugOutputWindow
  {

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public static void WriteLine (string line)
    {
      // 
      // Output specified line to the 'Debug' output window pane.
      // 

      IVsOutputWindow outputWindow = Package.GetGlobalService (typeof (SVsOutputWindow)) as IVsOutputWindow;

      IVsOutputWindowPane debugWindowPane;

      outputWindow.CreatePane (VSConstants.OutputWindowPaneGuid.DebugPane_guid, "General", 1, 0);

      outputWindow.GetPane (VSConstants.OutputWindowPaneGuid.DebugPane_guid, out debugWindowPane);

      debugWindowPane.Activate ();

      debugWindowPane.OutputString (line + Environment.NewLine);
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  }

  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

}

