using System;
using System.Collections.Generic;
using System.Runtime.InteropServices;

using AndroidPlusPlus.Common;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

namespace AndroidPlusPlus.VsDebugEngine
{

  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public class DebugEnumerator<T, I> where I: class
  {

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    private readonly T [] m_data;

    private uint m_position;

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public DebugEnumerator (T [] data)
    {
      m_data = data;

      m_position = 0;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public DebugEnumerator (List <T> data)
    {
      m_data = data.ToArray ();

      m_position = 0;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public int Clone (out I ppEnum)
    {
      // 
      // Creates an enumerator that contains the same enumeration state as the current enumerator.
      // 

      ppEnum = null;

      return DebugEngineConstants.E_NOTIMPL;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public int GetCount (out uint pcelt)
    {
      // 
      // Gets the number of ports in an enumerator.
      // 

      pcelt = (uint)m_data.Length;

      return DebugEngineConstants.S_OK;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public int Next (uint celt, T [] rgelt, ref uint celtFetched)
    {
      // 
      // Retrieves a specified number of ports in an enumeration sequence.
      // 

      return Move (celt, rgelt, out celtFetched);
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public int Reset ()
    {
      // 
      // Resets an enumeration sequence to the beginning.
      // 

      lock (this)
      {
        m_position = 0;

        return DebugEngineConstants.S_OK;
      }
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public int Skip (uint celt)
    {
      // 
      // Skips a specified number of ports in an enumeration sequence.
      // 

      uint celtFetched;

      return Move (celt, null, out celtFetched);
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    private int Move (uint celt, T[] rgelt, out uint celtFetched)
    {
      lock (this)
      {
        int hr = DebugEngineConstants.S_OK;

        celtFetched = (uint)m_data.Length - m_position;

        if (celt > celtFetched)
        {
          hr = DebugEngineConstants.S_FALSE;
        }
        else if (celt < celtFetched)
        {
          celtFetched = celt;
        }

        if (rgelt != null)
        {
          for (int c = 0; c < celtFetched; c++)
          {
            rgelt[c] = m_data[m_position + c];
          }
        }

        m_position += celtFetched;

        return hr;
      }
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  }

  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

}
